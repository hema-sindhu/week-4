const num1 = 10;
const num2 = 20;

const greaterThan = num1 > num2;
const lessThan = num1 < num2;
const equalTo = num1 == num2;
const notEqualTo = num1 != num2;
const greaterThanOrEqualTo = num1 >= num2;
const lessThanOrEqualTo = num1 <= num2;

console.log(`${num1} > ${num2}: ${greaterThan}`);
console.log(`${num1} < ${num2}: ${lessThan}`);
console.log(`${num1} == ${num2}: ${equalTo}`);
console.log(`${num1} != ${num2}: ${notEqualTo}`);
console.log(`${num1} >= ${num2}: ${greaterThanOrEqualTo}`);
console.log(`${num1} <= ${num2}: ${lessThanOrEqualTo}`);
